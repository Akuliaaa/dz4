        <h1 class="top-text">HISTORY IT TECHNIC</h1>
        <div class="box">
            <p>The story of creating a website for an educational and recreational center for schoolchildren began with the realization of the need to offer youth not only educational but also recreational opportunities for comprehensive development. The inspiration for the project came from the idea of creating a universal platform that could combine education, creativity, leisure, and socialization, providing students with access to a variety of courses, events, clubs, and activities.
            </p>
            <br>
            <h1 class="left">1. Idea and Concept</h1>
            <p>
            The project started with the idea of creating an educational space that would not only be a source of knowledge but also a place for rest and socializing. The educational and recreational center for schoolchildren was meant to combine elements of school, clubs, training, and leisure, along with opportunities for extracurricular development.
            The goal of the website was to create an online platform that would offer information about courses, sections, studios, and events organized by the center, as well as allow parents and students to register for activities, track schedules, and stay informed about events. The website was also designed to integrate with social media to create a community.
            </p>
            <br>
            <h1 class="right">2. Audience Research</h1>
            <p>Before starting the development, research was conducted through surveys among parents, teachers, and schoolchildren themselves. This helped identify the key needs of the target audience. It turned out that students were interested not only in academic development but also in activities that allowed them to express their individuality, participate in team games, and develop their talents and hobbies.
            <br>
            For parents, safety, accessibility, and ease of registration for activities and courses were the most important factors. Additionally, the website needed to be easy to use for parents who might not be very tech-savvy.
            </p>
            <br>
            <h1 class="left">3. Development Process</h1>
            <p>The development process began with designing the website's structure. A team of web developers and designers created a user-friendly interface that allowed users to quickly find information about courses, schedules, and events. The website was made mobile-responsive since both students and their parents often use smartphones to search for information.</p>
            <br>
            <p>The website design was bright and appealing to teenagers, incorporating elements that gave a sense of creativity and activity. The main sections of the website included:</p>
            <br>
            <ul>
                <li>Courses and sections — with detailed descriptions of each course, age groups, and schedules.</li>
                <li>Events — with announcements of cultural and sports events, competitions, and festivals.</li>
                <li>Online registration — an easy-to-use system for signing up for activities and classes</li>
                <li>News and blogs — for communication between students, parents, and educators.</li>
                <li>Forums and discussions — to foster an active community.</li>
            </ul>
            <br>
            <p>For parents, a separate section was added with information on child safety, terms of participation, and privacy policies.</p>
            <br>
            <h1 class="right">4. Testing and Launch</h1>
            <p>
            After the development was completed, the website was tested with a small group of users. Tests were conducted on ease of navigation, page loading speed, and the accuracy of the registration system. Based on feedback, necessary improvements and corrections were made.
            <br>
            The official launch of the website took place a few months after development. During this time, a marketing campaign was carried out, including email newsletters and social media ads to attract the target audience.
            </p>
            <br>
            <h1 class="left">5. Maintenance and Growth</h1>
            <p>
            After the launch, the website continued to evolve. The list of available courses and events was regularly updated, new sections and features were added. For example, a rating and review system for courses was implemented, and video content with recommendations from instructors and students was also added.
            <br>
            The project continues to grow, attracting more schoolchildren and their parents. In the future, the website plans to introduce online classes and remote learning options, as well as expand the platform for international competitions and events.
            <br>
            Thus, the educational and recreational center's website has become an essential part of the educational process, combining both learning and leisure for children while providing a convenient platform for parents.
            </p>
